    private void menu_add_buttonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_menu_add_buttonActionPerformed
        // TODO add your handling code here:
        // get inventory items from check boxes in table
        ArrayList<Integer> inventory_items = new ArrayList<Integer>();
        for (int i = 0; i < menuAddInventoryItemsTable.getRowCount(); i++) {
            if ((boolean) menuAddInventoryItemsTable.getValueAt(i, 0)) {
                try {
                    ResultSet rs = database.getInventory();
                    while (rs.next()) {
                        if (rs.getString("name").equals(menuAddInventoryItemsTable.getValueAt(i, 1))) {
                            inventory_items.add(rs.getInt("id"));
                        }
                    }
                } catch (Exception e) {
                    System.out.println(e);
                }
            }
        }
        if (!isNumber(id_menu_add_textField.getText()) || (name_menu_add_textField.getText().isBlank())
                || !isNumber(price_menu_add_textField.getText())) {
            throwErrorMessage();
        } else {
            if (database.addMenuItem(Integer.parseInt(id_menu_add_textField.getText()),
                    name_menu_add_textField.getText(), Double.parseDouble(price_menu_add_textField.getText()))
                    && database.updateMenuItemInventoryItems(Integer.parseInt(id_menu_add_textField.getText()), inventory_items)) {
                throwSuccessMessage(1);
                id_menu_add_textField.setText("");
                name_menu_add_textField.setText("");
                price_menu_add_textField.setText("");

            } else {
                throwErrorMessage();
            }
        }
    }// GEN-LAST:event_menu_add_buttonActionPerformed

